<h5>Reglas del juego:</h5>
<p>Una partida de dados puede estar formada por una o varias jugadas, tantas como el jugador quiera.</p>
<p>En cada jugada hay que simular la tirada de dos dados. Para ganar el juego hay que llegar a una puntuación entre 50 y 60 puntos.</p>
<p>Las puntuaciones se obtendrán al sumar los valores obtenidos en los dados. Con las siguientes excepciones:</p>
<ul>
    <li>Si salen dos unos puntúa 5.</li>
    <li>Si salen dos dos puntúa 10.</li>
    <li>Si salen dos tres puntúa 15.</li>
    <li>Si salen dos cuatro puntúa 20</li>
    <li>Si salen dos cinco puntúa 25.</li>
    <li>Si salen dos seis puntúa 30.</li>
</ul>
